class student{
    constructor(id=null, name=null, year=null, course=null){
        this.id=id
        this.name=name
        this.year=year
        this.course=course
    }

    getId(){
        return this.id
    }
    getName(){
        return this.name
    }
    getYear(){
        return this.year
    }
    getCourse(){
        return this.course
    }


}